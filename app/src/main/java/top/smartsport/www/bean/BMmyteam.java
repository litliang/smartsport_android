public class BMmyteam {
    private String id;
    private String team_name;
    private String members;
    private String coath_name;
    private String coath_mobile;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getTeam_name() {
        return team_name;
    }

    public void setTeam_name(String team_name) {
        this.team_name = team_name;
    }

    public String getMembers() {
        return members;
    }

    public void setMembers(String members) {
        this.members = members;
    }

    public String getCoath_name() {
        return coath_name;
    }

    public void setCoath_name(String coath_name) {
        this.coath_name = coath_name;
    }

    public String getCoath_mobile() {
        return coath_mobile;
    }

    public void setCoath_mobile(String coath_mobile) {
        this.coath_mobile = coath_mobile;
    }
}
